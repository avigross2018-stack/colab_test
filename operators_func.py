# This function adds two numbers together
def multi(num_1,num_2):
    return num_1 * num_2


# This function divides two numbers
def divide(num_1,num_2):
    return num_1 / num_2


# This function adds two numbers together
def add(num_1,num_2):
    return num_1 + num_2


def sub(a,b):
    '''
    Docstring for sub
    subtract a and b
    
    :param a: number
    :param b: number
    '''
    return a - b

def power(a,b):
    '''
    Docstring for power
    a in power of b
    :param a: number
    :param b: number
    '''
    return a ** b

def root(a):
    '''
    Docstring for root
    a in power of (0.5)
    :param a: int number
    '''
    return a ** 0.5
