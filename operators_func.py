def sub(a,b):
    '''
    Docstring for sub
    subtract a and b
    
    :param a: number
    :param b: number
    '''
    return a - b

def power(a,b):
    '''
    Docstring for power
    a in power of b
    :param a: number
    :param b: number
    '''
    return a ** b

def root(a):
    '''
    Docstring for root
    a in power of (0.5)
    :param a: int number
    '''
    return a ** 0.5